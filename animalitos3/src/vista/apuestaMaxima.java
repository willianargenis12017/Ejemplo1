    void mostrardatos(String valor){  // METODO PARA CREAR LA TABLA
           Connection con = null;
        try {
            
            con = getConection();
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("CODIGO");
        modelo.addColumn("APUESTA MAXIMA");
        tbproductos.setModel(modelo);
        
        String sql = " ";
        if(valor.equals(" ")){ // si el campo valor esta vacio entonces
           
            sql = "SELECT * FROM apuesta_maxima"; // muestra todos los datos
        
        }else{ // si el campo valor no esta vacio entonces
            
            sql = "SELECT * FROM apuesta_maxima WHERE monto_maximo='"+valor+"'"; // selecciona el que tenga el codigo iguan al campo valor
            
        }
   // String codigo = "0001";
        
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(sql);  //Almacena los datos de la consulta que vamos a realizar
        String [] datos = new String [2]; //Variable donde se guardan los datos que nos traemos de la tabla 
    while(rs.next()){                   //Mostramos los datos
        
          datos[0] = rs.getString(1); // Lo que esta en rs, lo guardo en la posicion 0 del arreglo dato
          datos[1] = rs.getString(2);
          
          modelo.addRow(datos); // Capturamos la fila con la informacion 
    }
    
        tbproductos.setModel(modelo); // Mostramos los datos en la tabla 
            
        } catch (Exception e) {
          }
    
       
  }
    
      

