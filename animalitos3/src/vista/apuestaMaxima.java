    private void btngrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngrabarActionPerformed
        
        Connection con = null;
        
        try{
            
            con = getConection();
            ps = con.prepareStatement("INSERT INTO apuesta_maxima(monto_maximo )VALUE (?)");
            //ps.setString(1, txtcod.getText());
            
            ps.setString(1, txtMonto.getText());
            
            
            int res = ps.executeUpdate();
           
            mostrardatos(" "); // metodo para que cuando grabe, se muestren todos los datos de la tabla
            
            if(res > 0){
                
                JOptionPane.showMessageDialog(null, "Monto registrado");
             
            }else {
                
                JOptionPane.showMessageDialog(null, "Error al guardar");
              
            }
            
            con.close();
        }
        catch(Exception e){
            
            System.out.println("");
            JOptionPane.showMessageDialog(null, "Ya existe un producto con ese codigo");
            
            
        }
        
        limpiarCampos();
        
        
    }//GEN-LAST:event_btngrabarActionPerformed

